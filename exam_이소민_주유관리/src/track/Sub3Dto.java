package track;

public class Sub3Dto {

	private String oildate, oilname, count, cost;

	public Sub3Dto(String oildate, String oilname, String count, String cost) {
		super();
		this.oildate = oildate;
		this.oilname = oilname;
		this.count = count;
		this.cost = cost;
	}

	public String getOildate() {
		return oildate;
	}

	public String getOilname() {
		return oilname;
	}

	public String getCount() {
		return count;
	}

	public String getCost() {
		return cost;
	}
	
	
}
