package track;

public class Sub2Dto {
	private String saleno, oildate, oilname, amount, paytype, custname, custno, custtel, creditcart, oilcost;

	public Sub2Dto(String saleno, String oildate, String oilname, String amount, String paytype, String custname,
			String custno, String custtel, String creditcart, String oilcost) {
		super();
		this.saleno = saleno;
		this.oildate = oildate;
		this.oilname = oilname;
		this.amount = amount;
		this.paytype = paytype;
		this.custname = custname;
		this.custno = custno;
		this.custtel = custtel;
		this.creditcart = creditcart;
		this.oilcost = oilcost;
	}

	public String getSaleno() {
		return saleno;
	}

	public String getOildate() {
		return oildate;
	}

	public String getOilname() {
		return oilname;
	}

	public String getAmount() {
		return amount;
	}

	public String getPaytype() {
		return paytype;
	}

	public String getCustname() {
		return custname;
	}

	public String getCustno() {
		return custno;
	}

	public String getCusttel() {
		return custtel;
	}

	public String getCreditcart() {
		return creditcart;
	}

	public String getOilcost() {
		return oilcost;
	}

	

	
	
	
}
