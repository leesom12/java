package dto;

public class Sub2Dto {
	private String v_jumin, v_name, m_no, v_time, v_area, v_confirm;

	public Sub2Dto(String v_jumin, String v_name, String m_no, String v_time, String v_area, String v_confirm) {
		super();
		this.v_jumin = v_jumin;
		this.v_name = v_name;
		this.m_no = m_no;
		this.v_time = v_time;
		this.v_area = v_area;
		this.v_confirm = v_confirm;
	}

	public String getV_jumin() {
		return v_jumin;
	}

	public String getV_name() {
		return v_name;
	}

	public String getM_no() {
		return m_no;
	}

	public String getV_time() {
		return v_time;
	}

	public String getV_area() {
		return v_area;
	}

	public String getV_confirm() {
		return v_confirm;
	}
	
	
}
