package dto;

public class EtcDto {

	private String no, group_no, title, reg_id, reg_name, reg_date;
	private int depth;
	
	
	
	public String getNo() {
		return no;
	}
	public String getGroup_no() {
		return group_no;
	}
	public String getTitle() {
		return title;
	}
	public String getReg_id() {
		return reg_id;
	}
	public String getReg_name() {
		return reg_name;
	}
	public String getReg_date() {
		return reg_date;
	}
	public int getDepth() {
		return depth;
	}
	
	
}
